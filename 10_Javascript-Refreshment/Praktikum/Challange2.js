function tambah(angka1, angka2) {
  return angka1 + angka2;
}
console.log(tambah(2, 3)); // Output: 5
console.log(tambah(10, 5)); // Output: 15

function kurang(angka1, angka2) {
  return angka1 - angka2;
}
console.log(kurang(2, 3)); // Output: 1
console.log(kurang(10, 5)); // Output: 5

function kali(angka1, angka2) {
  return angka1 * angka2;
}
console.log(kali(2, 3)); // Output: 6
console.log(kali(10, 5)); // Output: 50

function bagi(angka1, angka2) {
  return angka1 / angka2;
}
console.log(bagi(2, 6)); // Output: 3
console.log(bagi(10, 2)); // Output: 5

function modulus(angka1, angka2) {
  return angka1 % angka2;
}
console.log(modulus(1, 2)); // Output: 1